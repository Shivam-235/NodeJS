console.log("Arithmetic Operators:");
let a = 10, b = 3;
console.log(`a + b = ${a + b}`);
console.log(`a - b = ${a - b}`);
console.log(`a * b = ${a * b}`);
console.log(`a / b = ${a / b}`);
console.log(`a % b = ${a % b}`);

console.log("\nAssignment Operators:");
let c = 5;
console.log(`c = ${c}`);
c += 2;
console.log(`c += 2 => ${c}`);
c *= 3;
console.log(`c *= 3 => ${c}`);

console.log("\nComparison Operators:");
console.log(`a == b: ${a == b}`);
console.log(`a != b: ${a != b}`);
console.log(`a > b: ${a > b}`);
console.log(`a < b: ${a < b}`);

console.log("\nLogical Operators:");
let x = true, y = false;
console.log(`x && y: ${x && y}`);
console.log(`x || y: ${x || y}`);
console.log(`!x: ${!x}`);

console.log("\nBitwise Operators:");
console.log(`a & b: ${a & b}`);
console.log(`a | b: ${a | b}`);
console.log(`a ^ b: ${a ^ b}`);
console.log(`~a: ${~a}`);
console.log(`a << 1: ${a << 1}`);
console.log(`a >> 1: ${a >> 1}`);